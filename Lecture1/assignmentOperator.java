package Lecture1;

public class assignmentOperator {
    public static void main(String[] args) {
        int x=5;
        x+=3; //x=x+3
        System.out.println(x);
        int p=9;
        p-=3; //p=p-3
        System.out.println(p);
        int a=9;
        a*=3; //a=a*3
        System.out.println(a);
    }
}
