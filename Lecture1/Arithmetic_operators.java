package Lecture1;

public class Arithmetic_operators {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        System.out.println("A=5 and B=10");
        int c=a+b;
        System.out.println("c=a+b "+c);
        c=a-b;
        System.out.println("c=a-b "+c);
        c=a*b;
        System.out.println("c=a*b; "+c);
        c=b/a;
        System.out.println("c=b/a; "+c);
        c=a%b;
        System.out.println("c=a%b; "+c);
        c=++a;
        System.out.println("c=++a; "+a);
        c=--a;
        System.out.println("c=--a; "+a);

    }
}