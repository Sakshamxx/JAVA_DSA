package Lecture1;

public class Grade {
    public static void main(String[] args) {
        int marks=90;
        if(marks>=80){
            System.out.println("GRADE:A");
        }else if(marks>=70){
            System.out.println("GRADE:B");
        }else if(marks>=60){
            System.out.println("GRADE:C");
        }else if(marks>=40){
            System.out.println("GRADE:D");}
            else{
                System.out.println("GRADE:F");
        }
    }
}
