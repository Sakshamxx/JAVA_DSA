package Lecture1;

public class Assignment_operator {
    public static void main(String[] args) {
        int x=5;
        x+=3;
        System.out.println(x);
        x-=3;
        System.out.println(x);
        x*=3;
        System.out.println(x);
        x/=3;
        System.out.println(x);
        x%=3;
        System.out.println(x);
    }
}
